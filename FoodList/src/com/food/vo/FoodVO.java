public class FoodVO {
	private int ingredient_id;
	private String ingredient_name;
	
	public int getIngredient_id() {
		return ingredient_id;
	}
	public void setIngredient_id(int tIngredient_id) 
	{
		this.ingredient_id = tIngredient_id;
	}
	public String getIngredient_name()
	{
		return ingredient_name;
	}
	public void setIngredient_name(String tIngredient_name)
	{
		this.ingredient_name = tIngredient_name;
	}
}
